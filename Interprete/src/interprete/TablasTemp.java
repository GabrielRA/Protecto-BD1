    private void butVerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butVerActionPerformed

        try {
 //Para establecer el modelo al JTable
            DefaultTableModel modelo = new DefaultTableModel();
            this.jtQuery.setModel(modelo);
            //Para ejecutar la consulta
            Statement s = Conexion.conn.createStatement();
            //Ejecutamos la consulta
            String consulta = "Select * From " + combTablas.getSelectedItem();
            ResultSet rs = s.executeQuery(consulta);
            //Obteniendo la informacion de las columnas que estan siendo consultadas
            ResultSetMetaData rsMd = rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();
            //Establecer cabezeras el nombre de las columnas
            for (int i = 1; i <= cantidadColumnas; i++) {
             modelo.addColumn(rsMd.getColumnLabel(i));
            }
            //Creando las filas para el JTable
            while (rs.next()) {
             Object[] fila = new Object[cantidadColumnas];
             for (int i = 0; i < cantidadColumnas; i++) {
               fila[i]=rs.getObject(i+1);
             }
             modelo.addRow(fila);
            }
            rs.close();
           } catch (Exception ex) {
            ex.printStackTrace();
           }
        // TODO add your handling code here:
    }//GEN-LAST:event_butVerActionPerformed

    private void cargarItem(){
        String nombreTabla;
        //this.combTablas.addItem(nombreTabla);       
        try{
            Statement consulta =(Statement) Conexion.conn.createStatement();
            ResultSet tablas = consulta.executeQuery("select name from sysobjects where type='U' AND name NOT IN ('sysdiagrams')");
            
            while (tablas.next()){
                nombreTabla = (String) tablas.getObject("name");
                this.combTablas.addItem(nombreTabla); 
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Error sql no se pueden leer datos");
        }
    } 
